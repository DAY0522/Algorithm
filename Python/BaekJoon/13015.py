# 13015번 별 찍기 - 23
# https://www.acmicpc.net/problem/13015

N = int(input())
print('*'*N + ' '*(2*(N-1)-1) + '*'*N)
for i in range(1, N-1):
    print(' '*i + '*' + ' '*(N-2) + '*' + ' '*(2*(N-(i+1))-1) + '*' + ' '*(N-2) + '*')
print(' '*(N-1) + '*' + ' '*(N-2) + '*' + ' '*(N-2) + '*')
for i in range(N-2, 0, -1):
    print(' '*i + '*' + ' '*(N-2) + '*' + ' '*(2*(N-(i+1))-1) + '*' + ' '*(N-2) + '*')
print('*' * N + ' ' * (2 * (N - 1) - 1) + '*' * N)
